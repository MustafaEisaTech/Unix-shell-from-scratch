#include<stdio.h>
#include<stdlib.h>
#include<sys/wait.h>
#include "parser.h"

int lsh_launch(char **args){
    pid_t pid, wpid;
    int status;

    pid = fork();
    if (pid == 0){
        if (execvp(args[0], args) == -1){
            perror("lsh");
        }
        exit(EXIT_FAILURE);
    }else if (pid < 0){
        perror("lsh");
    }else{
        do{
            wpid = waitpid(pid, &status, WUNTRACED);
        } while (!WIFEXITED(status) && !WIFSIGNALED(status));
        
    }
    
    
}